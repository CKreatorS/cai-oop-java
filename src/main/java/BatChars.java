abstract class BatChars {
    public String name;  
    public String alias;
    public String gimic; 
    protected boolean hasCostume;

    public BatChars(String name, String alias, String gimic) {
        this.name = name; 
        this.alias = alias; 
        this.gimic = gimic; 
        this.hasCostume = true; 
    }
}
